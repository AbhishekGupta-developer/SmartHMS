package com.myorganisation.smarthms.dto;

import com.myorganisation.smarthms.model.enums.Gender;
import com.myorganisation.smarthms.model.enums.Shift;
import com.myorganisation.smarthms.model.enums.Specialization;

public class DoctorResponseDTO {

    private Long id;
    private String name;
    private Gender gender;
    private Specialization specialization;
    private Shift shift;
    private String email;
    private String phone;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Gender getGender() {
        return gender;
    }

    public void setGender(Gender gender) {
        this.gender = gender;
    }

    public Specialization getSpecialization() {
        return specialization;
    }

    public void setSpecialization(Specialization specialization) {
        this.specialization = specialization;
    }

    public Shift getShift() {
        return shift;
    }

    public void setShift(Shift shift) {
        this.shift = shift;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
